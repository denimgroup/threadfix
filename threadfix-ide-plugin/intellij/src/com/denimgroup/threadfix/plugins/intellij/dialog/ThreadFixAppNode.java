class ThreadFixAppNode extends CheckedTreeNode {

    @Nonnull
    public String getName() {
        return name;
    }

    @Nullable
    public String getId() {
        return id;
    }

    private final String name, id;

    public ThreadFixAppNode(@Nonnull String name, @Nullable String id) {
        this.name = name;
        this.id = id;
    }

}
